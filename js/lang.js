/**
 * creates a cookie called 'lang' that stores the language for 90 days
 * @param value (string) ISO lang code
 */
function createLangCookie(value) {
    // list the options and check that lang is one
    const supportedLangs = ['da', 'de', 'en', 'es','fr', 'nl', 'it'];
    if (!supportedLangs.includes(value)) {
        throw 'Not a supported language';
    }
    // get the expiry time (90 days)
    let date = new Date();
    date.setTime(date.getTime() + (90 * 24 * 60 * 60 * 1000));
    let expires = "; expires=" + date.toGMTString();
    // create the cookie
    document.cookie = "lang=" + value + expires + "; path=/";
}

/**
 * Get the current language as set in the cookie
 * @returns {string || undefined} ISO language code or undefined if no cookie
 */
function getCookieLang() {
    const value = `; ${document.cookie}`;
    const parts = value.split(`; lang=`);
    if (parts.length === 2) return parts.pop().split(';').shift();
}

