/**
 * Swaps the language of the page
 * @param lang {string} value of lang ('en', 'da', 'fr', ect.)
 * @param fromDashboard {boolean} function is being called form index.php
 * @static
 */
function changeLang(lang, fromDashboard = true) {
    // list the options and check that lang is one, if not default to english
    const supportedLangs = ['da', 'de', 'en', 'es','fr', 'nl', 'it'];
    if (!supportedLangs.includes(lang)) {
        lang = 'en';
    }

    // change the lang
    document.documentElement.setAttribute('lang', lang);
    // translate all the spot where it can
    $(".translate").each((index) => {
        let elem = $($(".translate")[index]);
        elem.text( arrLang[lang][elem.attr('data-key')] );
    });
    // update the labels for the scan history table
    if (fromDashboard) {
        try {
            scanHistoryTable.langLabel = arrLang[lang]["viewResults"];
            // change the labels on the tables
            resChart.data.labels = [arrLang[lang]["compliant"], arrLang[lang]["nonComp"], arrLang[lang]["untagged"]];
            resChart.update();
            historyChart.data.datasets[0].label = arrLang[lang]["untagged"];
            historyChart.data.datasets[1].label = arrLang[lang]["nonComp"];
            historyChart.data.datasets[2].label = arrLang[lang]["compliant"];
            historyChart.update();

            // refresh tables, used to make 'true' and 'false' values translate
            let temp1 = liteScan.files;
            liteScan.files = [];

            let temp2 = fileViewer.files;
            fileViewer.files = [];

            // if the scan is Pro make sure to change the headers for the files table
            if (viewStatus === null && startScan.ScanType === 'Pro') {  // For when the dashboard initially renders because viewStatus wont be created yet
                changeProTableHeaders(lang);
            }
            else if (viewStatus === null && startScan.ScanType === 'Lite') {
                changeLiteTableHeaders(lang)
            }
            else if (viewStatus.isPro) {   // For when the language is changed via the menu
                changeProTableHeaders(lang);
            } else {
                changeLiteTableHeaders(lang)
            }

            // update the local attribute for the files grid
            fileViewer.lang = lang;

            setTimeout(() => {
                liteScan.files = temp1;
                fileViewer.files = temp2;

                // updating the locale attribute for files grid will break the scroll bar (top) which is why it needs reset
                listenersSet = false;
                resizeScrollPro();
            }, 10);
        } catch (err) {
            console.error(err);
        }
    }
    createLangCookie(lang);
}

